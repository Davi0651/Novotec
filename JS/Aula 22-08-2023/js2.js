//Soma de três números:

let num1 = 2;
let num2 = 4;
let num3 = 6;
let soma1 = num1 + num2 + num3;
console.log(soma1); //Resultado: 12

//Soma de números decimais:

let decimal1 = 2.5;
let decimal2 = 1.3;
let soma2 = decimal1 + decimal2;
console.log(soma2); //Resultado: 3.8

//concatenação de strings:

let texto1 = "Olá";
let texto2 = "Mundo";
var numero = 2;
let mensagem = texto1 + " " + texto2 + " " + numero;
console.log(mensagem); //Resultado: "Olá Mundo 2"